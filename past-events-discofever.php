         <h3>&quot;DISCO FEVER" This Far Out Event Brings in over $100,000 </h3>
         <p>Polyester leisure suits, bell-bottoms, cat suits, and platform shoes were just a few items worn by many community members on Feb. 28, 2015.  Disco Fever, Onslow Memorial Hospital's 7th Annual Fundraiser, supported Project Panda Bear and Project 2020.  With the support of the community, we were able to surpass our goal and actually purchase 5 new Panda Infant Warmers for the Nursery and Labor & Delivery units at Onslow Memorial Hospital.</p>
         <p>Thanks to the skills of the hospital maintenance department and the decorating committee, including Karin Dickerson, Maria Dempster, and Jennifer Fields, the American Legion had the look and feel of a 1970's Discotheque.</p>
         <p>During this "groovy" event, guests had the opportunity to bid on items in the silent and live auctions.  Kudos to Gayle Alvis and Paula Garris for organizing the amazing auction.  Guests were also able to dance "The Hustle" with the A-Town, A-List and take crazy photos with Props Photo Booth.  The dance floor was packed all evening as guests showed off their 1970's attire.</p>
         <p>The Foundation would like to offer a special thanks to our Hall of Heritage supporters: Louis and Doris Sewell and Marine Federal Credit Union.  Thank you to all of our sponsors including; dinner sponsor Golden Corral, OMH employees, physicians, local businesses and individuals.  Your support for quality healthcare is truly commendable.  We appreciate all that you do for our community and our hospital.</p>
         <p>We are grateful for the remarkable work of our Foundation Board, the Events Committee, the local area Marines, Donors, and Community Members that help make the event possible and help to impact the entire community by providing the best healthcare, right here at home.</p>
         <p>We look forward to your support for the Roaring 20's, the Foundation's 2016 Annual Event.</p>
         <p>Mark your calendars for Feb. 27, 2016!!</p>
         
