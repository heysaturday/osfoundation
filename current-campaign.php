             <h2>Building for Babies</h2>
                         
             <p><img src="images/Building-for-Babies-logo_web.png" align="left" width="150"/> 
<p>The Foundation’s Building for Babies campaign continues throughout 2020 with an added new perspective: grandparents and their grandbabies! For children, a grandparent can be a source of comfort, wisdom, stability – and let’s face it, just plain fun! Free from many of the restrictions and responsibilities new parents feel, grandparents often say they are finally able to “relax” and enjoy a whole new life of precious moments. You’ll often hear a new grandparent say they now feel able to be more fully “present” for experiences they may have missed out on with their own children.</p> 

 <p>And did you know that caring for grandchildren can help ward off depression, boost social connections and keep us mentally sharp? According to several important studies cited in a recent article from U.S.News & World Report, grand-parenting rewards us with a treasure trove of physical, mental and spiritual benefits – not the least of which is mutual unconditional love. </p>
 
 <p>So let’s celebrate those grandbabies! Let’s welcome them into a new and functionally beautiful Maternal Child Services Floor – which includes Labor & Delivery, Nursery, Neonatal Intensive Care Unit and Pediatrics – delivering a “5-star experience” in every way imaginable. “Ultimately, I’d love to see a whole new wing built with minimal intervention birthing suites that give us easy access to our existing Operating Room if a routine delivery turns unexpectedly emergent. In the immediate future, we hope to create updated birthing suites that allow for true family bonding time before, during and after delivery,” said Dr. Donna Anderson, an obstetrics & gynecology specialist in Jacksonville.</p>
 
 <p>“Our providers and nurses are some of the best and most accomplished in the region. Everyone’s focus at Onslow Memorial Hospital is always ‘healthy mom, healthy baby’ and now we see the need for our facilities to reflect that top-notch care,” Dr. Anderson added. </p>
 
 <p>Our Foundation’s Annual Giving Campaign is persevering in its goal to support significant improvements to our Maternal Child Services Floor, transforming it into a nurturing haven for healthy moms and healthy babies … babies that will grow into the pride and joy of every expectant grandparent!</p>



 
