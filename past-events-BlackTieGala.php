             <h3>With Glamour and Flair, the Black Tie Gala raised over $130,000!</h3>

             <p>The Free proved to be the perfect fit for the spectacular 10th Anniversary Black Tie Gala! This upscale party blended modern style with their authentic southern roots.  They effortlessly engaged the dance floor with high-energy sets that pulled from the best of rock &amp; roll, pop hits, country, and classic R&B.</p>

             <p>The Black Tie Gala guests could not praise this band enough – we hear you!</p>

             <p>When they weren’t dancing, Black Tie Gala patrons found time to bid on a fantastic array of silent auction items using their smartphones. Many guests also had lots of fun taking photos in the Props Photo Booth.</p>

             <p>Golden Corral did a wonderful job as always –again this year, the focus was on heavy hors d’oeuvres, available throughout the night, which everyone loved. Lots of compliments were given for the carving station and shrimp cocktails. And oh, the red velvet cake was the ultimate anniversary celebration.</p>

             <p>Did I mention the tuxes and dresses?! You really did us proud! From the ladies’ lovely floor length dresses, to the men’s sharp tuxedos, you brought glamour and sophistication to Jacksonville!</p>

             <p>The Foundation would like to offer a special thanks to our Hall of Heritage supporters: The Sewell Family and Marine Federal Credit Union. Thank you to all of our sponsors including; dinner sponsor Golden Corral, title sponsor Johnston Pain Management, OMH employees, physicians, local businesses and individuals. Your support for quality healthcare is truly commendable. We appreciate all that you do for our community and our hospital.</p>

             <p>We are grateful for the remarkable work of our Foundation Board, the Events Committee, the local area Marines/Navy Volunteers, Donors, Volunteers and Community Members that help make the event possible and help to impact the entire community by providing the best healthcare, right here at home.</p>

             <p>Thank you all for a friend-filled night of fundraising to benefit Building for Babies!</p>

	

             <p>Lee Ann Thomas<br>

             Executive Director<br>

             Onslow Memorial Hospital Foundation</p>



