         <h3>Hats off to the “Onslow Derby” for raising over $106,000 for Project 317 Bistro
</h3>
         <p>The New Royals proved to be the perfect fit for the spectacular Onslow Derby! This blue-ribbon party band showcased a versatile trio of lead vocalists and a backing band comprised of Atlanta’s great music scene and NBC’s hit show America’s Got Talent.
</p>
         <p>When they weren’t dancing, Derby patrons found time to bid on a fantastic array of silent auction items using their smartphones. Many guests also had lots of fun taking photos in the Props Photo Booth.
</p>
         <p>Golden Corral did a wonderful job as always – but this year, the focus was on heavy hors d’oeuvres, available throughout the night, which everyone loved. Lots of compliments were given for the Derby-themed carving station and shrimp cocktails. And oh, those Mint Julep cupcakes! (Let’s not say how many we ate!)
 </p>
         <p>Did I mention the hats?! You really did us proud! From the ladies’ lovely wide-brimmed hats, to the gents’ stylish Fedoras, you brought the southern Derby tradition home to Jacksonville!
</p>
         <p>The Foundation would like to offer a special thanks to our Hall of Heritage supporters: The Sewell Family and Marine Federal Credit Union. Thank you to all of our sponsors including; dinner sponsor Golden Corral, title sponsor Johnston Pain Management, OMH employees, physicians, local businesses and individuals. Your support for quality healthcare is truly commendable. We appreciate all that you do for our community and our hospital.
</p>
<p>We are grateful for the remarkable work of our Foundation Board, the Events Committee, the local area Marines/Navy Volunteers, Donors, Volunteers and Community Members that help make the event possible and help to impact the entire community by providing the best healthcare, right here at home.
</p>
         <p>Thank you all for a friend-filled night of fundraising to benefit <b>Project 317 Bistro!</b>
</p>
         <p>Lee Ann Thomas<br>
Executive Director<br>
Onslow Memorial Hospital Foundation
</p>

