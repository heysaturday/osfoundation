                <h2><span class="bold"><img src="images/News082214.jpg" width="300" height="240" align="left" />Onslow Memorial Hospital Foundation Recognized as Purple Heart Organization</span></h2>

                <p>The Purple Heart is the oldest decoration in present day use and was initially created as the Badge of Military Merit by General George Washington in 1782. The Purple Heart was the first American service award or decoration made available to the common soldier and is specifically awarded to any member of the United States Armed Forces wounded or killed in combat with a declared enemy of the United States. The Military Order of the Purple Heart was chartered by Congress with a mission to foster an environment of goodwill among the combat wounded veteran members and their families, promote patriotism, support legislative initiatives and most importantly – make sure we never forget. After meeting with the Beirut Memorial Chapter 642 , Military Order of the Purple Heart, the Onslow Memorial Hospital Foundation signed the Purple Heart Proclamation and pledged to support those who served through which together, we will build strong businesses and communities. The OMH Foundation proudly proclaims it our honor to be recognized as a Purple Heart Organization in the City of Jacksonville.</p>
