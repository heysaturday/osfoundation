                    <h2><span class="bold"><img src="images/059.jpg" width="250" height="auto" align="left" style="padding-right:10px;"/>Donor Spotlight</span></h2>

                    <p>Jackie Mitchell has a long-standing history in the medical field. She worked in Labor and Delivery in New Orleans and at Onslow Memorial Hospital, then worked for obstetrician and gynecologist Paul Williams for eight years at the Crist Clinic for Women. In 1993, Mitchell moved to the Health Department, and in 1996 she became a nurse practitioner. In 2013 Jackie Mitchell retired after working for the Health Department for 20 years.<br><br></p>



                        <p>Since her retirement, Mitchell, along with board members Melissa St. Clair and Angie Silance, has continued to work tirelessly with the Onslow Women’s Health Fund (OWHF). The OWHF started in 2007 after a concerned group of women came together to brainstorm ways to provide medical funding for women who fell between the cracks of available health screenings and treatment. To date, OWHF has helped over 60 women with diagnostic testing and medical services. Generous partners for this cause included organizations such as Onslow Memorial Hospital, Onslow County Health Department, and the Onslow County Department of Social Services.<br><br></p>



                        <p>Unfortunately, after serving the community for over a decade, the Onslow Women’s Health Fund dissolved its operations on October 25, 2018. Over the years, OWHF received fiduciary sponsorship by the North Carolina Community Foundation (NCCF), Onslow Community Health Improvement Process (CHIP), and has granted monies to cover the costs of mammograms, diagnostic screenings, and lab work for dozens of under/uninsured women in Onslow County. Additionally, the OWHF granted money to Onslow Memorial Hospital Radiology and   Julie’s Pink Warrior Project.<br><br></p>



                        <p>Despite past success and strong collaboration from community partners, budget constraints and lack of infrastructure, the OWHF Advisory Committee voted to dissolve the organization. As a final order of business, Onslow Women's Health Fund presented grants to the following local organizations:<br><br></p>
                        
                        
                        <p class="indent">Onslow Community Outreach toward their Capital Campaign debt reduction</p>

                        <p class="indent">United Way of Onslow County toward their health track</p>

                    <p class="indent">Onslow Memorial Hospital Foundation - Julie's Pink Warrior Project</p><br>
                   



                        <p>Julie’s Pink Warrior Project will continue the great work done by the Onslow Women’s Health Fund by providing free screening mammograms for the underinsured and uninsured women of Onslow County.
