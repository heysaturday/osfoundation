         <h3>Whole Lotta Love (for Biker Bash) raises over $100,000</h3>
         <p>Leather-clad patrons sporting tattoos, custom choppers, skull artwork, and rock ’n’ roll music set the tone for Biker Bash, this year’s annual fundraiser for the OMH Foundation. The sold-out event welcomed over 700 community members and raised more than $100,000 for Project 2020, a long-term campaign to support the need for a new patient tower.</p>
         <p>Thanks to the skills of the hospital maintenance department and the decorating committee, led by Lee Ann Thomas, the American Legion building had the look and feel of a movie backdrop from “Ghost Rider”. Local motorcycle enthusiasts loaned their “colors” and T- shirts to add to the décor. Many thanks go to area Marines, led by SgtMaj Joe Houle, for helping with this amazing transformation.</p>
         <p>Golden Corral, a major sponsor of this year’s fund-raiser, provided a wonderful dinner, complemented by an amazing cupcake display in the fiery colors of the event. Biker Bash marked the sixth event for Golden Corral, which is fast approaching the $100,000 mark for inkind contributions in services and food to the Foundation.</p>
         <p>After a welcome by Foundation Chair Jeff Clark and an invocation from Honorary Chair LaRue Hambrick, the evening kicked into gear with silent and live auctions, organized by Gayle Alvis and Jimmie Ervin, and emceed by George Lanvermeier. Jewelry, golf packages, beach houses, limousine packages and granite vanity tops topped the list of most-wanted items. (Special thanks to Tim Chadwick and his small team of tech-savvy volunteers for streamlining the check-in and check-out process!) Funds from the auctions, “Biker Bars,” photo booth and a few games of chance generated nearly $15,000.</p>
         <p>The evening went into overdrive with the high-energy and nonstop performance of the A-Town A-List, an Atlanta-based band that kept the dance floor in motion until closing time with a string of favorites from line dance to pop to funk to rock.</p>
         <p>“We are so grateful for the remarkable generosity that this community provides, for an event that will impact the entire community by providing the best healthcare available in our area,” said Clark. “The time and dedication of the events committee is truly remarkable.” Amy Sousa, Vice President Public Relations/Marketing, echoed those comments, adding: “Many hands make light work. The success of this event is a credit to the countless hours donated by many volunteers who serve on the Foundation board, hospital board, on the events committee, and as cheerleaders within their hospital departments or physician practices. Each and every volunteer makes this possible.”</p>
         <p>The Foundation would like to offer special thanks to our Hall of Heritage supporters – Marine Federal Credit Union and Louis &amp; Doris Sewell – as well as all of our other sponsors. We couldn’t have done it without everyone’s help.</p>
         <p>P.S.: Start finding those 1970s polyester leisure suits and metallic dresses ... next year’s theme is Saturday Night Fever Disco!</p>
