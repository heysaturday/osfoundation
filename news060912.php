      <h2><span class="donars">Published  05-18-2011</span></h2>
      <p>On May 18th, renovations began at Onslow Memorial Hospital to fulfill the necessary requirements to accept a mobile PET scanner. The project is expected to reach completion by the end of July. </p>
      <p>        The mobile PET scanner allows oncologists to determine if a patient's tumor has metastasized, which simple means to see if the cancer cells have spread to other organs of the body. The unit will provide services to six patients every other Sunday.</p>
      <p>        The sponsors from Cowboy Up! helped to provide these necessary renovations that are needed to bring this advanced technology to Onslow county to better serve our local cancer patients and their families! Thank you again for your support.</p>
