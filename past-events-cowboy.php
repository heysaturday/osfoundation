<p><br />
  On Saturday, February 26th, 800 guests Boot Scoot and boogied at the OMH Foundation's third annual event, Cowboy Up!, that generated more than $150,000 in support of the Radiation Oncology Center's need for a PET Scanner.<br />
  <br />
&quot;Howdy, partner!&quot;, &quot;Ride 'em, Cowboy!&quot;, and &quot;Whoa, Nellie!&quot; were a few common sayings heard from many that attended the shindig that evening, at the Jacksonville American Legion. Attendees dressed the theme by purchasing cowboy hats, boots and chaps for the hoedown.<br />
<br />
Cowboys and Cowgirls line danced their boots off to an array of music provided by Taco and Maria's DJ Services, while others enjoyed listening to Ryan Shupe and the RubberBand play their up-tempo country tune. A few were escorted to jail by the volunteer deputies on hand, but were released after donating $10 for bail!<br />
<br />
Special thanks to the long list of dedicated sponsors, the enthusiastic Foundation Decorations Committee, the devoted volunteers, auction item donors and the many others who helped to make this event such a great success. &quot;We are so very grateful to everyone involved in Cowboy Up!,&quot; said OMH Foundation Executive Director, Tim Strickland. &quot;We recognize the time, dedication and compassion of the Foundation Board and Events Committee members, and the remarkable generosity of the many donors who have made a meaningful difference for local cancer patients.&quot;
<br />
<br />
Did you miss out on all the fun? The Foundation Board and Events Committee are already hard at work planning the next exciting event, so mark your calendar for February 25, 2012!<br />
<br />
