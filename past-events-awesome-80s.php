          <h2 style="text-align:center;">Our ‘Awesome ’80s party’ was, like, totally righteous!!</h2>
          <p>On Saturday February 23, the OMH Foundation’s ‘Awesome ’80s’ gala event rocked Jacksonville’s American Legion Building, in the words of Lionel Richie, “all night long” – and all for our precious Onslow County babies!</p>



<p>“Building for Babies,” the Foundation’s 2018-2019 Annual Giving Campaign, was initiated in 2018, and continues through 2019, to raise funds for significant improvements to the Maternal Child Services Floor which includes Labor & Delivery, Nursery, Neonatal Intensive Care Unit, and Pediatrics.</p>



<p>“Thanks to the overwhelming support of this community, we’ve raised a combined total of nearly $235,000 from last year and this year’s events and are now able to start real improvements to our Maternal Child Services Floor,” reports Foundation Executive Director Lee Ann Thomas. “We could not have done this without the help of all our OMH Foundation Board Members, our Events Committee Members, and all our dedicated volunteers.”</p>



<p>The ’80s show band, The Deloreans, played all the mega hits from the era of glitz and glam.  “Their energy was unbelievable. They had multiple costume changes and literally never took a break all night,” Thomas said. “People were on the dance floor nonstop!”</p>



<p>“The Decorations Committee led by Karin Dickerson outdid themselves,” Thomas added. “It was a total flashback to the ’80s!”</p>



<p>The well-attended extravaganza included a first-time ‘wine pull’ with wrapped wine bottles donated by Baked, Bottle and Brew, Empire Distributing, and OMH Foundation Board Members, Beth Purcell, Mike Williams and Stan Walker. Some bottles were worth $20 and some upwards of $100. By buying a $20 ticket, guests took a chance by pulling a mystery bottle from the wine rack (consolations prizes available to anyone pulling a less expensive bottle!)</p>



<p>Meanwhile, the silent and live auctions (thanks to Gayle Alvis, Paula Garris, and Tydrea Russell) yielded highly sought after items, such as a Carolina Panthers Jersey donated by the Panthers and signed by Ollie Green, a Pac Man video game, and a Belle Etoile Sterling jewelry set with hand painted turquoise and Italian enamels donated by Bradley’s Jewelers.</p>



<p>As always, a truly great dinner was provided by Golden Corral and a well-stocked bar attended to guests thanks to all our retired USMC officers hosting as volunteer bartenders!</p>



<p>“We could not have done this without our Hall of Heritage Sponsors – Marine Federal Credit Union, Golden Corral and Louis and Doris Sewell,” Thomas said. “Drs. Scott and Carol Johnston personally donated $10,000, in addition to their practices donating $1,000 each. The generosity and vision of this medical community never fails to astound us in such heart-warming ways!”</p>



	<p>When the music finally ended (don’t ask the time!) a core of very special people – Jacksonvill’es ‘own’ SgtMaj Joe Houle USMC (Ret), Col John Kopka USMC (Ret) and GySgt Stan Walker USMC (Ret) – and all their volunteer marines – stayed on for the tiring work of returning the American Legion Building to its former self. “Not only are these great people here to help us set up, they are the last people standing,” Thomas laughed. “They know the true meaning of service, and we are especially blessed and grateful for them.”</p><p></p>
