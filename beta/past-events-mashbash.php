<p><br />
  What was M*A*S*H Bash?! You may ask... On February 28, 2009, the OMH Foundation hosted their first gala, M*A*S*H Bash, named in celebration of the last episode of M*A*S*H, which aired on February 28, 1983. Scrubs, Hawaiian Shirts and Klinger Dresses were just a few items worn by the festive crowd that evening.</p>
<p>The Foundation generated $200,000 at M*A*S*H Bash in support of the new Radiation Oncology Center. A buffet dinner, cash bar, and an auction were components listed on the agenda that evening. Special Guest, Jamie Farr, arrived to entertain the crowd. A few lucky attendees even had the opportunity to get a snap shot with the celebrity!<br />
