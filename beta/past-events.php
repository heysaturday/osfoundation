           <h2><br />
             <span class="bold">Guests have a “groovy time” at Hippie Hippie Shake</span></h2>
<p>Bell bottom jeans, tie dye shirts and go-go boots were just a few items worn by many community members on the evening of February 25, 2012. Hippie Hippie Shake, the Onslow Memorial Hospital Foundation’s 1960s themed event raised over $138,000 to support Project 2020, a 7-year, $2 million campaign to support the need for a new patient tower at OMH.</p>
<p>During the psychedelic event, community hippies had the opportunity to bid on items in the silent and live auctions, dance with go-go dancers, and groove to Spirit of the Radio. The dance floor was packed all evening as guests showed off their 1960s attire. </p>
<p> Thank you to all of our sponsors (including event sponsor Golden Corral), OMH employees, and physicians for your support for Project 2020. Your support for quality healthcare is truly commendable. We appreciate all that you do for our community and our hospital.</p>
<p>Also, special thanks to the volunteers, Marines and Sailors who worked so hard to make this event a great success. The Foundation Decorations Committee, headed by Ms. Karin Dickerson, turned the dreary American Legion into a blast from the past with tissue flowers, peace signs and smiley faces. The silent auction, organized by Ms. Lee Ann Thomas was full of exciting items to bid on all evening!</p>
<p>“This is the fourth year in a row that the great people of Jacksonville have come together to support yet another charitable event - an event that will impact the entire community by helping to provide the best health care available in our area,” said OMH Foundation Board Chair Jeff Clark.  “The event would not have been possible without the dedication of the Foundation board and the Events Committee Members. We are extremely grateful for the community involvement and are looking forward to another fun event next year!”<br />
  <br />
</p>
