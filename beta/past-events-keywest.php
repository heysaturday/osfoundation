<p><br />
  Hula hoops, straw hats, flip-flops and frozen margaritas - Jacksonville has never seen it get so hot in February!    </p>
<p>At Key West Island Fest - the OMH Foundation's Feb. 27 fund-raising gala - the tropical food, entertainment and ambience swept over 600 attendees away to the Caribbean for a delightful evening of fun, all for a great cause!</p>
<p>The Foundation is pleased to say that we surpassed our fund-raising goal of $100,000 for the evening, and earned a bit more than $120,000 in support of the new Radiation Oncology Center. In this tough fund-raising climate, it is amazing to say that over 100 businesses, cancer survivors, civic groups, hospital employees and concerned citizens showed their support by sponsoring the event!</p>
<p>Countless others donated auction items and other in-kind contributions, bought tickets to attend, and participated in event-day excitement like souvenir photo-ops and the live auction - with 100 percent of the proceeds benefitting the Foundation.</p>
<p>We are truly humbled by the passionate support shown to this worthy cause, and the Foundation has been able to make great strides over the past couple of years because of our volunteers and donors. Additionally, special thanks to Project Chair LaRue Hambrick and Foundation Board Chair Rik Pugh for their dedication and enthusiasm.</p>
