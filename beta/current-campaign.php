         <h2>Project 2020</h2>
<p>The OMH Foundation has launched its next fund-raising campaign, Project 2020. Project 2020 is a 7-year, $2 million campaign to support the need for a new patient tower at Onslow Memorial Hospital.</p>
<p>The current tower is almost 40 years old and was built to serve the population of Onslow County in the 1970s. Since then, the population has increased by over 70 percent. Over the past 40 years, the healthcare industry has also dramatically enhanced medical care equipment. The current infrastructure struggles to accommodate this needed equipment.</p>
<p><img src="images/foundation-towerinset.gif" alt="Project 2020" width="325" height="250" align="right" />With Onslow Memorial Hospital’s emphasis on patient- and family-centered care, we recognize that families want to be involved in patient care, now more than ever. The older, smaller rooms are not large enough to accommodate family members. The new space will be designed to include patient families as key caregivers and partners in their loved ones’ healing process.</p>
<p>With your help, we can bring the latest technology, equipment, and facilities to Onslow County and provide the best medical care in a family-centered environment for you and your family. You can truly make a difference in the quality of healthcare for our community by supporting Project 2020 today.</p>
<p>Ways to support Project 2020 include sponsoring the OMH Foundation’s annual event, or making a Major Gift of $25,000 or more. If you wish to learn more about the benefits of your support through these gift options, please contact the OMH Foundation at (910) 577-2651.</p>
